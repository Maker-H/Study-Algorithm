package sec01_IO_Operations;


import java.util.*;
public class operation05_operations_10869 {
    public static void main(String[] args) {
        int A,B;
        Scanner scanner = new Scanner(System.in);
        
        A = scanner.nextInt(); B = scanner.nextInt();
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);
        System.out.println(A%B);
        scanner.close();
    }
}
